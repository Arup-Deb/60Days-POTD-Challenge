class Solution{
  public:
    // function should return the number of turns required to go from first node to second node 
    int ans=0;
    
    int getTurns(struct Node* root, int first, int second,int from){
        if(!root)return 0;
        
        int left = getTurns(root->left,first,second,1);
        int right = getTurns(root->right,first,second,2);
        
        if(root->data==first || root->data==second){
            if(left || right)return 0;
            return 1;
        }
        // cout<<root->data<<" : "<<left<<" "<<right<<endl;
        //this is your LCA
        if(left!=0 && right!=0){
            ans++;
            return 0;    
        }else if(left!=0){
            if(from==2) ans++;
            return 1;
        }else if(right!=0){
            if(from==1) ans++;
            return 1;
        }else return 0;
    }
    
    int NumberOFTurns(struct Node* root, int first, int second)
    {
        if(first!=second)
            getTurns(root,first,second,0);
        
        return ans;
    }
};

